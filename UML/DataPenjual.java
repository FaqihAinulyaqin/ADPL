public class DataPenjual {
    // Attributes
    private String usernamePenjual;
    private String passwordPenjual;

    // Constructor
    public DataPenjual(String usernamePenjual, String passwordPenjual) {
        this.usernamePenjual = usernamePenjual;
        this.passwordPenjual = passwordPenjual;
    }

    // Getter methods
    public String getUsernamePenjual() {
        return usernamePenjual;
    }

    public String getPasswordPenjual() {
        return passwordPenjual;
    }
}
